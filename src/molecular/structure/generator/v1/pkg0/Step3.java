    public Hashtable GetFromJtextfieldList(List<JTextField> listOfTextFields  ){
        Integer compteur=0;
        boolean error =false;
        Hashtable HT = new Hashtable();
        Integer atom1=0;
        for (JTextField JTAtom : listOfTextFields) {     
            compteur++;
            try{
                atom1 =Integer.parseInt(JTAtom.getText().toString().trim());
                if( atom1 > this.molStep3.GetNumberOfAtoms()){
                    error=true;
                }                
            }catch(Exception E )
            {
                error=true;
            }
           
            if (!error){
                HT.put(compteur.toString(), atom1);  

            }else{
                return null;
            }
            
        }
        return HT; 
        
    }
   
