    private void jLabelValidateStep1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelValidateStep1MouseClicked
        // TODO add your handling code here:
        
        this.molStep1.SetNumberOfAtoms(0);//NumberOfAtoms = 0;
        this.molStep1.SetNumberOfAtomsNoHydrogen(0);//NumberOfAtomsNoHydrogen = 0;        
        int compteur = 0;
        boolean error =false;
        for (JTextField JTSymbole : listOfTextFieldsSymbole) {       
            String Symbole = JTSymbole.getText().toString().trim().toUpperCase();
            if (! this.molStep1.GettableOfValenceStandard().containsKey(Symbole)){ // si le symbole ne pas un symbole chimique
                error=true;
                 JOptionPane.showMessageDialog(this, "Atom "+Symbole+" not found", "Error", JOptionPane.ERROR_MESSAGE);
                }  
            compteur++;           
        }
         if (!error){//Hashtable Molecular formula get in
            compteur=0;
            Integer compteur2=0;
            for (JTextField JTSymbole : listOfTextFieldsSymbole) {       
                String Symbole = JTSymbole.getText().toString().trim().toUpperCase();
                int Number = Integer.parseInt(listOfTextFieldsNumber.get(compteur).getText()); 
                 this.molStep1.GettableOfMolecularFormula().put(Symbole, Number);
                 if (!Symbole.trim().toUpperCase().equals("H")) {
                        for(int i=1; i<=Number; i++){  
                            //System.out.println(compteur2+Symbole);
                             this.molStep1.GettableOfSymboles().put(compteur2.toString() ,Symbole);                                                        compteur2++;
                        }
                 }
                compteur++;
            }
            
            compteur = 0;// Determining of NumberOfAtomsNoHydrogen and NumberOfAtoms
            
            for (JTextField Number : listOfTextFieldsNumber) {
                int c=  this.molStep1.GetNumberOfAtoms();
                c+= Integer.parseInt(Number.getText());
                this.molStep1.SetNumberOfAtoms(c) ;
                if (!listOfTextFieldsSymbole.get(compteur).getText().toString().trim().toUpperCase().equals("H")) {
                    int d =  this.molStep1.GetNumberOfAtomsNoHydrogen();
                        d += Integer.parseInt(Number.getText());
                         this.molStep1.SetNumberOfAtomsNoHydrogen(d);
                }
                compteur++;
            }
             jLabelValidateStep1Go.setVisible(true);
         }
             
            
           
    
    }//GEN-LAST:event_jLabelValidateStep1MouseClicked

    private void jSpinner1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinner1StateChanged
        // TODO add your handling code here:
        molecule moleculesought= new molecule();
        int counttextfields = Integer.parseInt(jSpinner1.getValue().toString());
        listOfTextFieldsSymbole.clear();
        listOfTextFieldsNumber.clear();

        if (counttextfields <= 0) {
            jLabelValidateStep1.setVisible(false);
            jLabelValidateStep1Go.setVisible(false);
            jPanel7.removeAll();
        } else {
            // JTextfield "jt"+counttextfiels = new JTextfield();
            jPanel7.removeAll();
            // jPanel7.setPreferredSize(new Dimension(400, 100));
            jPanel7.setLayout(new GridBagLayout());
            //jPanel7.setBorder(LineBorder.createBlackLineBorder());
            //jPanel7.setLayout(new GridBagLayout());

            //GridBagConstraints textFieldConstraints = new GridBagConstraints();
            for (int i = 0; i < counttextfields; i++) {
                final JTextField JTsymbole = new JTextField();
                final JTextField JTNumber = new JTextField();
                JTsymbole.setText("Atom");               
                JTsymbole.setFont(new java.awt.Font("Segoe UI", 0, 22)); 
                JTNumber.setText("00");  
                JTNumber.setFont(new java.awt.Font("Segoe UI", 0, 22)); 

                JTsymbole.addFocusListener(new FocusListener() {
                    public void focusGained(FocusEvent e) {
                        JTsymbole.setText("");
                    }

                    public void focusLost(FocusEvent e) {
                        if(JTsymbole.getText().toString().isEmpty()){
                            JTsymbole.setText("Atom");
                        }
                    }
                });

                JTNumber.addFocusListener(new FocusListener() {
                    public void focusGained(FocusEvent e) {
                        JTNumber.setText("");
                    }

                    public void focusLost(FocusEvent e) {
                        if(JTNumber.getText().toString().isEmpty()){
                            JTNumber.setText("00");
                        }
                    }
                });

                /*
                * textFieldConstraints.gridx = i; textFieldConstraints.fill =
                * GridBagConstraints.HORIZONTAL; textFieldConstraints.weightx =
                * 0.5; textFieldConstraints.insets = new Insets(10, 10, 10,
                    * 10); textFieldConstraints.gridy =0;
                */
                listOfTextFieldsSymbole.add(JTsymbole);
                listOfTextFieldsNumber.add(JTNumber);
                jPanel7.add(JTsymbole);
                jPanel7.add(JTNumber);
            }
            jLabelValidateStep1.setVisible(true);
            jPanel7.updateUI();

        }

        moleculesought.SetNumberOfSymboles(counttextfields);//NumberOfSymboles = counttextfields;
        moleculesought.SetNumberOfAtoms(0);//NumberOfAtoms = 0;
        moleculesought.SetNumberOfAtomsNoHydrogen(0);//NumberOfAtomsNoHydrogen = 0;
        moleculesought.SetNumberOfSymboleNoHydrogen(0);//NumberOfSymboleNoHydrogen = 0;
        //VCSPMGG.mol=moleculesought;
    }//GEN-LAST:event_jSpinner1StateChanged

