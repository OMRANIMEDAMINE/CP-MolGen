        this.molStep4.ImposedFragments[0]=jRadioButton11.isSelected();
        this.molStep4.ImposedFragments[1]=jRadioButton12.isSelected();
        this.molStep4.ImposedFragments[2]=jRadioButton13.isSelected();
        this.molStep4.ImposedFragments[3]=jRadioButton14.isSelected();
        
        
        this.molStep4.ForbiddenFragments[0]=jRadioButton21.isSelected();
        this.molStep4.ForbiddenFragments[1]=jRadioButton22.isSelected();
        this.molStep4.ForbiddenFragments[2]=jRadioButton23.isSelected();
        this.molStep4.ForbiddenFragments[3]=jRadioButton24.isSelected();
          
       
             SwingWorker<Void, Void> mySwingWorker = new SwingWorker<Void, Void>(){
         @Override
         protected Void doInBackground() throws Exception {

            // mimic some long-running process here...
             
 
              int N = molStep4.GetNumberOfAtomsNoHydrogen();
                if (molStep4.ForbiddenFragments[0]==true||molStep4.ForbiddenFragments[1]==true||molStep4.ForbiddenFragments[2]==true||molStep4.ForbiddenFragments[3]==true){
                    //#-------------Forbidding Cycle 3--------------------#

                    if (molStep4.ForbiddenFragments[0]==true){
                            molStep4.ExplicitCycle3= CyclesGeneration.Cycles(N, 3);
                    }

                    if (molStep4.ForbiddenFragments[1]==true){
                            molStep4.ExplicitCycle4= CyclesGeneration.Cycles(N, 4);
                    }

                    if (molStep4.ForbiddenFragments[2]==true){
                            molStep4.ExplicitCycle5= CyclesGeneration.Cycles(N, 5);
                    }

                    if (molStep4.ForbiddenFragments[3]==true){
                            molStep4.ExplicitCycle6= CyclesGeneration.Cycles(N, 6);
                    }
                }


        
        
             
             
             // END long-running process 

           // Thread.sleep(SLEEP_TIME);
            return null;
         }
      };

      //Window win = SwingUtilities.getWindowAncestor((AbstractButton)evt.getSource());
      final JDialog dialog = new JDialog(this, "Molecular Data Analysing", Dialog.ModalityType.APPLICATION_MODAL);

      mySwingWorker.addPropertyChangeListener(new PropertyChangeListener() {

         @Override
         public void propertyChange(PropertyChangeEvent evt) {
            if (evt.getPropertyName().equals("state")) {
               if (evt.getNewValue() == SwingWorker.StateValue.DONE) {
                  dialog.dispose();
               }
            }
         }
      });
      mySwingWorker.execute();

      JProgressBar progressBar = new JProgressBar();
 
      
   
      
      progressBar.setIndeterminate(true);
      JPanel panel = new JPanel(new BorderLayout());
      
      
      //begin Personaliser
       panel.setBackground(new java.awt.Color(255, 255, 255));
     
       panel.setPreferredSize(new Dimension(330,50));
      // end personaliser 
      
      panel.add(progressBar, BorderLayout.CENTER);
      panel.add(new JLabel("Please wait, chargement ......."), BorderLayout.PAGE_START);
      dialog.add(panel);
      dialog.pack();
      dialog.setLocationRelativeTo(this);
      dialog.setVisible(true);
        
